// Iterate with loops
 var myArray = [];
var i = 0;
 while(i < 5) {
     myArray.push(i);
     i++;
 }
 console.log(myArray);


//  For Loops
var ourArray = [];
for (var i = 0; i < 5; i++) {
    ourArray.push(i);
}
console.log(ourArray)



